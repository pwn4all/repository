##########################################################################################
## 대용량 파일 업로드
##########################################################################################
brew install git-lfs


##########################################################################################
## 대용량 파일 업로드
##########################################################################################
cd packages
git rm -r --cached "*"

git lfs track "*.zip"

##########################################################################################
## 서버에 필요한 lfs 대용량 파일 설정
##########################################################################################
cat .gitattributes
*.zip filter=lfs diff=lfs merge=lfs -text

##########################################################################################
## .gitattributes 파일이 전달되고 나면, push 가능
##########################################################################################
git add .gitattributes
git add "file.zip"
git commit -m "Big File"
git push origin main
